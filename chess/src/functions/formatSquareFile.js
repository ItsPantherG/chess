function formatSquareFile(num) {
  if (num === 1) return "a";
  if (num === 2) return "b";
  if (num === 3) return "c";
  if (num === 4) return "d";
  if (num === 5) return "e";
  if (num === 6) return "f";
  if (num === 7) return "g";
  if (num === 8) return "h";
}

export default formatSquareFile;
